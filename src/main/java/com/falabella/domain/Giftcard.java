package com.falabella.domain;

public class Giftcard {

	private String giftcardNumber;
	private String messageSendingTime;
	private String messageReceptionTime;

	public String getGiftcardNumber() {
		return giftcardNumber;
	}

	public void setGiftcardNumber(String giftcardNumber) {
		this.giftcardNumber = giftcardNumber;
	}

	public String getMessageSendingTime() {
		return messageSendingTime;
	}

	public void setMessageSendingTime(String messageSendingTime) {
		this.messageSendingTime = messageSendingTime;
	}

	public String getMessageReceptionTime() {
		return messageReceptionTime;
	}

	public void setMessageReceptionTime(String messageReceptionTime) {
		this.messageReceptionTime = messageReceptionTime;
	}
}
